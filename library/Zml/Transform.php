            'З' => 'Z',    'з' => 'z',
            'И' => 'I',    'и' => 'i',
            'Й' => 'Y',    'й' => 'y',
            'К' => 'K',    'к' => 'k',
            'Л' => 'L',    'л' => 'l',
            'М' => 'M',    'м' => 'm',
            'Н' => 'N',    'н' => 'n',
            'О' => 'O',    'о' => 'o',
            'П' => 'P',    'п' => 'p',
