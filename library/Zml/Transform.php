            '>' => '',
            '?' => '',
            '^' => '',
            '|' => '-',
            '/' => '-',
            "\\" => '-',
            ' ' => '-',
        );
        $konform_temp = strtr($konform_temp, $transform);
        
        $transform = array(
            '---' => '-',
            '--'  => '-',
        );
        $konform_temp = strtr($konform_temp, $transform);
        
        return $konform_temp;
    }
}