            'Ю' => 'Yu',   'ю' => 'yu',
            'Я' => 'Ya',   'я' => 'ya',
        );
        $konform_temp = strtr($value, $transform);
        
        $transform = array(
            '(' => '',
            ')' => '',
            '{' => '',
            '}' => '',
            '[' => '',
            ']' => '',
            '!' => '',
