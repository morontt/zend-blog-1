{
    public static function ruTransform($value = null)
    {
        $transform = array(
            'А' => 'A',    'а' => 'a',
            'Б' => 'B',    'б' => 'b',
            'В' => 'V',    'в' => 'v',
            'Г' => 'G',    'г' => 'g',
